package tagl;

public class ListScore {
	private int score;
	private String valeur;
	
	public ListScore(int score, String valeur) {
		super();
		this.score = score;
		this.valeur = valeur;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}
	public String getString() {
		return valeur;
	}
	public void setString(String valeur) {
		this.valeur = valeur;
	}
	

}
